// Get all available geographies
export const GEOGRAPHIES = Object.keys(REGIONS_BY_GEO);

// Get regions for a specific geography
export const getRegionsByGeo = (geo: string): RegionWithGeo[] => {
  return REGIONS_BY_GEO[geo] || [];
};

export const getRegion = (id: string): Region | undefined => {
  return REGIONS.find(r => r.id === id);
};

export const getDefaultRegion = (): Region => {
  return REGIONS[0]; // US East by default
};

export const getDefaultGeo = (): string => {
  return 'Americas';
};
